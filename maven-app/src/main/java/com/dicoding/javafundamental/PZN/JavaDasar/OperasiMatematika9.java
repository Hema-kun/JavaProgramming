package com.dicoding.javafundamental.PZN.JavaDasar;

public class OperasiMatematika9 {

    public static void main(String[] args) {

        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        //assignment
        int c = 100;
        System.out.println(c+=10);

        //unary
        int d =100;
        System.out.println(d++);
        System.out.println(d--);
        System.out.println(-d);
        System.out.println(+d);
    }

}
